package foka.db;

public class HumanResource {

	private String humanName;
	private long fokCount;

	public String getHumanName() {
		return humanName;
	}

	public void setHumanName(String humanName) {
		this.humanName = humanName;
	}

	public long getFokCount() {
		return fokCount;
	}
	
	void setFokCount(long fokCount) {
		this.fokCount = fokCount;
	}
	
}
